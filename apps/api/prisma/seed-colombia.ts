async function main() {
  console.log('🌎 Sembrando catálogos territoriales de Colombia...');

  // Upsert Colombia
  await prisma.pais.upsert({
    where: { codigo: 'CO' },
    update: {},
    create: { codigo: 'CO', nombre: 'Colombia' },
  });

  // Upsert all departments
  for (const dept of DEPARTAMENTOS) {
    await prisma.departamento.upsert({
      where: { codigo: dept.codigo },
      update: { paisCodigo: 'CO' },
      create: {
        codigo: dept.codigo,
        nombre: dept.nombre,
        paisCodigo: 'CO',
      },
    });
  }

  // Upsert all municipalities
  let totalMuns = 0;
  for (const [deptCodigo, muns] of Object.entries(MUNICIPIOS)) {
    for (const mun of muns) {
      await prisma.municipio.upsert({
        where: { codigo: mun.codigo },
        update: {},
        create: {
          codigo: mun.codigo,
          codigoDepartamento: deptCodigo,
          nombre: mun.nombre,
        },
      });
      totalMuns++;
    }
  }

  console.log(`✅ ${DEPARTAMENTOS.length} departamentos y ${totalMuns} municipios de Colombia cargados.`);
}

main()
  .catch((e) => {
    console.error('❌ Error:', e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
